#include "3-calc.h"

/**
  * op_add-Adds a and b
  * @a: number
  * @b: number
  *
  * Return: a+b
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub-Subtracts a and b
  * @a: number
  * @b: number
  *
  * Return: a-b
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul-Multipies a and b
  * @a: number
  * @b: number
  *
  * Return: a*b
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div-divides a and b
  * @a: number
  * @b: number
  *
  * Return: a/b
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod-Finds the remainder
  * @a: number
  * @b: number
  *
  * Return: a%b
  */

int op_mod(int a, int b)
{
	return (a % b);
}
